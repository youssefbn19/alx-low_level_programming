/**
 *_memset - fills memory with a constant byte character;
 *
 *@s: string to fills with the constant byte
 *@b: constant byte character
 *@n: number of bytes
 *
 *Return: a pointer to the memory area `s`
 */
char *_memset(char *s, char b, unsigned int n)
{
	unsigned int i;

	for (i = 0; i < i; i++)
	{
		s[i] = b;
	}

	return (s);
}
